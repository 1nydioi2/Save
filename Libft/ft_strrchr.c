#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	i;

	if (c == '\0')
		return ((char *)(str + ft_strlen(str)));
	if (!str)
		return (NULL);
	i = (ft_strlen(str) - 1);
	while (i >= 0)
	{
		if (str[i] == (unsigned char)c)
			return ((char *)&str[i]);
		i--;
	}
	return (NULL);
}
