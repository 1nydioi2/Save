#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char	c;

	if (!fd)
		return ;
	if (nb == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (nb < 0)
	{
		nb = -nb;
		write (fd, "-", 1);
	}
	if (nb > 9)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
	{
		c = (nb + 48);
		write(fd, &c, 1);
	}
	return ;
}
